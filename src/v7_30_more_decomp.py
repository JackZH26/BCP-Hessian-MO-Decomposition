h1_pass = 0; h1_total = 0
h2_pass = 0; h2_total = 0
no_pi_count = 0

for r in new_results:
    eps = r['eps']
    dpi = r['delta_pi_num']
    dsi = r['delta_sigma_num']
    dtot = r['delta_bader']
    
    if eps > 0.05:
        h1_total += 1
        if abs(dpi) < 0.005:  # No π MO contribution
            sign_str = "σ-only"
            if dsi > 0: h1_pass += 1
        elif dsi > 0 and dpi < 0:
            sign_str = "✓"
            h1_pass += 1
        else:
            sign_str = "✗"
    elif eps < 0.01:
        h2_total += 1
        if abs(dpi + dsi) < 0.01 + 0.01*max(abs(dpi), abs(dsi)):
            sign_str = "DEGEN✓"
            h2_pass += 1
        else:
            sign_str = "DEGEN?"
    else:
        sign_str = "border"
    
    print(f"{r['name'][:4]:5} {r['name']:32} {r.get('cat',''):12} "
          f"{eps:+8.4f}  {dtot:+8.4f} {dsi:+8.4f} {dpi:+8.4f}  {sign_str}")

print("\n" + "="*120)
