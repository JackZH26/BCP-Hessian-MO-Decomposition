new_results = []
for tup in TESTS_NEW:
    label = tup[0]
    atoms = tup[1]
    A, B = tup[2], tup[3]
    cat = tup[4] if len(tup) > 4 else "?"
    spin = tup[5] if len(tup) > 5 else 0
    charge = tup[6] if len(tup) > 6 else 0
    try:
        if spin > 0:
            print(f"\n[skipping {label} — open shell {spin}, would need ROHF]")
            continue
        r = analyze_decomp(label, atoms, A, B)
        r['cat'] = cat
        new_results.append(r)
    except Exception as e:
        print(f"\n!!! {label} FAILED: {str(e)[:80]}")

print("\n" + "="*120)
